#include "ft_printf.h"

char	*ft_tolower_all(char *str)
{
	int	i;

	i = 0;
	while (str[i] != '\0')
	{
		if ('A' <= str[i] && str[i] <= 'F')
			str[i] += 32;
		i++;
	}
	return (str);
}

